
set r1, 2000	// write ptr
generateStart:
	in r2, 9
	set [r1], r2
	add r1, 2
	cmp r1, 2400
	jb generateStart

set r0, 2000	// read ptr
generateTrail:
	in r2, 9	// random character
	and r2, 254
	
	set r3, [r0]
	shr r3, 8
	dec r3
	shl r3, 8
	
	set r4, r2
	or r4, r3
	set [r1], r4
	
	add r0, 2
	add r1, 2
	
	cmp r1, 31600
	jb generateTrail

xor r0, r0		// palette offset
switch:
	set r1, palette
	add r1, r0
	
	// save first color
	push r1
	push r1
	set r5, palette0
	set [r5], [r1]
	inc r5
	inc r1
	set [r5], [r1]
	inc r5
	inc r1
	set [r5], [r1]
	pop r1
	set [r1], 0
	inc r1
	set [r1], 0
	inc r1
	set [r1], 0
	pop r1
	
	// save last color
	push r1
	add r1, 765
	push r1
	set r5, palette255
	set [r5], [r1]
	inc r5
	inc r1
	set [r5], [r1]
	inc r5
	inc r1
	set [r5], [r1]
	pop r1
	set [r1], 255
	inc r1
	set [r1], 255
	inc r1
	set [r1], 255
	pop r1
	
	out 20, r1
	
	// restore colors
	set r5, palette0
	set [r1], [r5]
	inc r5
	inc r1
	set [r1], [r5]
	inc r5
	inc r1
	set [r1], [r5]
	add r1, 763
	set r5, palette255
	set [r1], [r5]
	inc r5
	inc r1
	set [r1], [r5]
	inc r5
	inc r1
	set [r1], [r5]
	
	add r0, 3
	mod r0, 768

	out 10, 33
delay:
	in r3, 10
	jnz delay
	
	jmp switch

